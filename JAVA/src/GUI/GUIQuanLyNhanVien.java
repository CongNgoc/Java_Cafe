    public void loadUser() {
        try {
            ArrayList<userDTO> list = userBUS.userAll();
            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            Object[] row = new Object[9];
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getId();
                row[1] = list.get(i).getName();
                row[2] = list.get(i).getUsername();
                row[3] = list.get(i).getPassword();
                row[4] = list.get(i).getPhone();
                row[5] = list.get(i).getEmail();
                row[6] = list.get(i).getAddress();
                row[7] = list.get(i).getGioitinh();
                row[8] = list.get(i).getChucvu();
                
                model.addRow(row);
            }
            dis_end(false);
            btnXoa.setEnabled(false);
            btnSua.setEnabled(false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUIQuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

