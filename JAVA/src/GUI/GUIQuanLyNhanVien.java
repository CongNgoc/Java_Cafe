    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        int i = jTable3.getSelectedRow();
        TableModel model = jTable3.getModel();
        txtID.setText(model.getValueAt(i, 0).toString());
        txtName.setText(model.getValueAt(i, 1).toString());
        txtUsername.setText(model.getValueAt(i, 2).toString());
        txtPass.setText(model.getValueAt(i, 3).toString());
        txtPhone.setText(model.getValueAt(i, 4).toString());
        txtEmail.setText(model.getValueAt(i, 5).toString());
        txtDiaChi.setText(model.getValueAt(i, 6).toString());
        if (model.getValueAt(i, 7).toString().equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        cBChucVu.setSelectedItem(model.getValueAt(i, 8).toString());
        dis_end(false);
    }//GEN-LAST:event_jTable3MouseClicked

