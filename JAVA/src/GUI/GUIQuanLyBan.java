    private void txtTimKiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemKeyReleased
        try {
            ArrayList<banDTO> list = banBUS.banSearch(txtTimKiem.getText(), txtTimKiem.getText());
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            Object[] row = new Object[5];
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getIdBan();
                row[1] = list.get(i).getTenKhuVuc();
                row[2] = list.get(i).getTenBan();
                row[3] = list.get(i).getDienGiai();
                row[4] = list.get(i).getTrangThai();
                model.addRow(row);
            }
            dis_end_BAN(false);
            txtID.setEditable(false);
            btnXoa.setEnabled(false);
            btnSua.setEnabled(false);
        } catch (ClassNotFoundException e) {
        }


    }//GEN-LAST:event_txtTimKiemKeyReleased

    private void btnThemNhieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNhieuActionPerformed
        try {
            int soLuong = (int) txtThemNhieu.getValue();
            if (soLuong > 0) {
                String sql = "SELECT * FROM `ban` ORDER BY `IdBan` DESC LIMIT 1";
                db.open();
                String tenBan = null;
                ResultSet rs = db.excuteQuery(sql);
                while (rs.next()) {
                    tenBan = rs.getString("TenBan");
                }
                db.close();
                String subID = tenBan.substring(4, tenBan.length());
                int a = Integer.parseInt(subID);
                String ban = "Bàn ";
                for (int i = 1; i <= soLuong; i++) {
                    int so = i + a;
                    String tenBanString = ban + so;
                    func fabc = new func();
                    try {
                        txtID.setText(fabc.MaTuDong("IdBan", "ban", "BAN"));
                    } catch (SQLException ex) {
                        Logger.getLogger(GUIQuanLyBan.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    String query = "INSERT INTO `ban`( `IdBan`, `TenKhuVuc`, `TenBan`, `DienGiai`, `TrangThai`) "
                            + "VALUES ('" + txtID.getText() + "','" + cBTenKhuVuc.getSelectedItem().toString() + "','" + tenBanString
                            + "','" + txtDienGiai.getText() + "','" + cBTrangThai.getSelectedItem().toString() + "')";
                    try {
                        db.open();
                        Statement st = db.conn.createStatement();
                        st.executeLargeUpdate(query);
                        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                        model.setNumRows(0);
                        loadBan();
                        db.close();
                    } catch (SQLException ex) {
                        db.displayError(ex);
                    }
                }
                JOptionPane.showMessageDialog(null, "Thêm " + soLuong + " bàn thành công!!!");
            } else {
                JOptionPane.showMessageDialog(null, "Số lượng bàn là số âm hoặc bằng 0, Vui lòng nhập lại");
            }
        } catch (HeadlessException | NumberFormatException | SQLException e) {
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUIQuanLyBan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnThemNhieuActionPerformed

