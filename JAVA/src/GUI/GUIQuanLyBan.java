    public void loadBan() throws ClassNotFoundException {
        ArrayList<banDTO> list = banBUS.banAll();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[5];
        banDTO ban = new banDTO();
        for (int i = 0; i < list.size(); i++) {
            ban = list.get(i);
            row[0] = ban.getIdBan();
            row[1] = ban.getTenKhuVuc();
            row[2] = ban.getTenBan();
            row[3] = ban.getDienGiai();
            row[4] = ban.getTrangThai();
            model.addRow(row);
        }
        dis_end_BAN(false);
        txtID.setEditable(false);
        btnXoa.setEnabled(false);
        btnSua.setEnabled(false);
    }

    public void LoadComboBoxTT() throws ClassNotFoundException {
        try {
            String sql = "SELECT TrangThai FROM `ban` GROUP BY TrangThai";
            db.open();
            ResultSet rs = db.excuteQuery(sql);
            while (rs.next()) {
                cBTrangThai.addItem(rs.getString("TrangThai"));
            }
            db.close();
        } catch (SQLException e) {
            db.displayError(e);
        }
    }
//Load ComboBox
    public void LoadComboBoxKV() throws ClassNotFoundException {
        try {
            db.open();
            ResultSet rs = db.excuteQuery("SELECT TenKhuVuc FROM `khuvuc` GROUP BY TenKhuVuc");
            while (rs.next()) {
                cBTenKhuVuc.addItem(rs.getString("TenKhuVuc"));
            }
            db.close();
        } catch (SQLException e) {
            db.displayError(e);
        }
    }
