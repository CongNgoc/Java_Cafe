    public void loadData() {
        try {
            ArrayList<banDTO> list = banBUS.banAll();
            for (int i = 0; i < list.size(); i++) {
                cbBanGop1.addItem(list.get(i).getTenBan());
            }
            db.open();
            String sql = "SELECT * FROM ban WHERE TrangThai = 'Có khách'";
            ResultSet rs = db.excuteQuery(sql);
            while (rs.next()) {
                cBBanGop2.addItem(rs.getString("TenBan"));
            }
            db.close();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(GUIChuyenBan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

