            String idBan = null;
            String sql = "Select IdBan from ban where TenBan = '" + cBBanGop2.getSelectedItem() + "'";
            db.open();
            ResultSet rs = db.excuteQuery(sql);
            while (rs.next()) {
                idBan = rs.getString("IdBan");
            }
            db.close();
            Home home = new Home();
            home.gopBan(idBan);
            Home.tabP.removeAll();
            home.loadBan();
