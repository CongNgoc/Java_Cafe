    public void loadThongTin(String id){
        try {
            ArrayList<userDTO> list = userBUS.userId(id);
            for (int i = 0; i < list.size(); i++) {
                txtID.setText(list.get(i).getId());
                txtName.setText(list.get(i).getName());
                txtAddress.setText(list.get(i).getAddress());
                txtEmail.setText(list.get(i).getEmail());
                txtPhone.setText(Integer.toString(list.get(i).getPhone()));
                txtUsername.setText(list.get(i).getUsername());
                if(list.get(i).getGioitinh().equals("Nam")){
                    rbtNam.setSelected(true);
                }
                else if(list.get(i).getGioitinh().equals("Nữ")){
                    rbtNu.setSelected(true);
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUIThongTinNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
