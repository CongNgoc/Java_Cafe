    private void btupdatedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdatedActionPerformed
        String gioiTinh = "";
        if(rbtNam.isSelected()){
            gioiTinh = "Nam";
        }else if(rbtNu.isSelected()){
            gioiTinh = "Nữ";
        }
        String query = "UPDATE `user` "
        + "SET `name`='" + txtName.getText() + "',"
        + "`phone`='" + txtPhone.getText() + "',`email`='" + txtEmail.getText() + "',"
        + "`address`='" + txtAddress.getText() + "',`gioitinh`='" + gioiTinh + "' WHERE id = '" + txtID.getText() + "'";
        try {
            db.open();
            if (db.excuteUpdate(query) == 1) {
                JOptionPane.showMessageDialog(null, "Cập nhật bàn thành công!!!");
            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại!!!");
            }
            db.close();
        } catch (SQLException | ClassNotFoundException ex) {
            db.displayError((SQLException) ex);
        }
        btupdated.setEnabled(false);
    }//GEN-LAST:event_btupdatedActionPerformed

