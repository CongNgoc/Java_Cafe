    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if (flag == 0) {
            //Thêm nhân viên
            String query = "INSERT INTO `khuvuc`( `IdKhuVuc`,`TenKhuVuc`, `DienGiai`, `TrangThai`) "
                    + "VALUES ('" + txtID.getText() + "','" + txtTenKV.getText() + "','" + txtDienGiai.getText() + "','" + cBTrangThai.getSelectedItem().toString() + "')";
            try {
                db.open();
                if (db.excuteUpdate(query) == 1) {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.setNumRows(0);
                    loadKhuVuc();
                    JOptionPane.showMessageDialog(null, "Thêm khu vực thành công!!!");
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thất bại!!!");
                }
                db.close();
            } catch (SQLException ex) {
                db.displayError(ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(GUIQuanLyKhuVuc.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            String query = "UPDATE `khuvuc` "
                    + "SET `TenKhuVuc`='" + txtTenKV.getText() + "',`DienGiai`='" + txtDienGiai.getText() + "',`TrangThai`='" + cBTrangThai.getSelectedItem().toString() + "' "
                    + "WHERE IdKhuVuc = '" + txtID.getText() + "'";
            try {
                db.open();
                if (db.excuteUpdate(query) == 1) {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.setNumRows(0);
                    loadKhuVuc();
                    JOptionPane.showMessageDialog(null, "Cập nhật bàn thành công!!!");
                } else {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại!!!");
                }
                db.close();
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(GUIQuanLyKhuVuc.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

