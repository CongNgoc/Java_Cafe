    public void loadKhuVuc() throws ClassNotFoundException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList<khuVucDTO> arr = new ArrayList<>();
        arr = khuVucBUS.khuVucAll();
        khuVucDTO kv = new khuVucDTO();
        Object[] row = new Object[4];
        for (int i = 0; i < arr.size(); i++) {
            kv = arr.get(i);
            row[0] = kv.getIdKhuVuc();
            row[1] = kv.getTenKhuVuc();
            row[2] = kv.getDienGiai();
            row[3] = kv.getTrangThai();

            model.addRow(row);
        }
    }

    public void LoadComboBox() {
        try {
            db.open();
            ResultSet rs = db.excuteQuery("SELECT TrangThai FROM `khuvuc` GROUP BY TrangThai");
            while (rs.next()) {
                cBTrangThai.addItem(rs.getString("TrangThai"));
            }
            db.close();
        } catch (ClassNotFoundException | SQLException e) {
            db.displayError((SQLException) e);
        }
    }
