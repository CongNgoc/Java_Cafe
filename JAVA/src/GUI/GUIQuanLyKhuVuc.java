    private void txtTimKiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemKeyReleased
        try {
            ArrayList<khuVucDTO> list = khuVucBUS.khuVucSearch(txtTimKiem.getText(), txtTimKiem.getText());
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            Object[] row = new Object[4];
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getIdKhuVuc();
                row[1] = list.get(i).getTenKhuVuc();
                row[2] = list.get(i).getDienGiai();
                row[3] = list.get(i).getTrangThai();
                model.addRow(row);
            }
            dis_end(false);
            btnXoa.setEnabled(false);
            btnSua.setEnabled(false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUIQuanLyKhuVuc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtTimKiemKeyReleased

